    private void btnCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarActionPerformed
        String ruta = txtArchivo.getText();
        if (ruta.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor selecciona un archivo primero.");
            return;
        }

        File archivo = new File(ruta);
        if (!archivo.exists()) {
            JOptionPane.showMessageDialog(this, "El archivo no existe.");
            return;
        }

        // ============================
        //  Validar extensión .csv
        // ============================
        String rutaLower = ruta.toLowerCase();
        if (!rutaLower.endsWith(".csv")) {
            JOptionPane.showMessageDialog(this,
                    "El archivo debe estar en formato CSV (.csv).",
                    "Formato inválido", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // ============================
        //  Modelo de la tabla visual
        // ============================
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Nombre");
        modelo.addColumn("Apellido");
        modelo.addColumn("Teléfono");
        modelo.addColumn("CC");
        modelo.addColumn("Correo");
        modelo.addColumn("Rol");
        jTable2.setModel(modelo);

        Conexion conexion = new Conexion();
        Connection conn = conexion.getConexion();

        if (conn == null) {
            JOptionPane.showMessageDialog(this, "No se pudo conectar a la base de datos.");
            return;
        }

        StringBuilder errores = new StringBuilder();

        try (BufferedReader br = new BufferedReader(new FileReader(archivo))) {

            // ===========================================
            // 1) LEER Y VALIDAR ENCABEZADO DEL CSV
            // ===========================================
            String header = br.readLine();
            if (header == null) {
                JOptionPane.showMessageDialog(this,
                        "El archivo CSV está vacío.",
                        "Archivo vacío", JOptionPane.WARNING_MESSAGE);
                return;
            }

            List<String> headerCampos = parseCsvLine(header);
            String[] requeridas = {"nombre", "apellido", "telefono", "cc", "correo", "rol"};

// Por si Excel mete BOM u otros caracteres raros
            boolean headerValido = headerCampos.size() >= requeridas.length;
            for (int i = 0; i < requeridas.length && headerValido; i++) {
                String valor = headerCampos.get(i)
                        .toLowerCase()
                        .replace("\uFEFF", "") // quita el BOM si viene pegado a "nombre"
                        .trim();

                // System.out.println("Columna " + i + ": '" + valor + "'");  // si quieres probar
                if (!valor.equals(requeridas[i])) {
                    headerValido = false;
                }
            }
            if (!headerValido) {
                JOptionPane.showMessageDialog(this,
                        "El archivo CSV debe tener como primeras columnas (en este orden):\n"
                        + "nombre, apellido, telefono, cc, correo, rol",
                        "Encabezado inválido", JOptionPane.WARNING_MESSAGE);
                return;
            }

            // ===========================================
            // 2) PROCESAR CADA FILA DE DATOS
            // ===========================================
            String linea;
            while ((linea = br.readLine()) != null) {
                linea = linea.trim();
                if (linea.isEmpty()) {
                    continue; // Ignorar líneas vacías
                }

                List<String> campos = parseCsvLine(linea);

                if (campos.size() < 6) {
                    errores.append("Fila incompleta: ").append(linea).append("\n");
                    continue;
                }

                String nombre = campos.get(0);
                String apellido = campos.get(1);
                String telefono = campos.get(2);
                String cc = campos.get(3);
                String correo = campos.get(4);
                String rolTexto = campos.get(5).toLowerCase();

                int idRol;
                switch (rolTexto) {
                    case "profesor":
                        idRol = 6;
                        break;
                    case "estudiante":
                        idRol = 4;
                        break;
                    default:
                        errores.append("Rol inválido '").append(rolTexto)
                                .append("' para el correo ").append(correo)
                                .append(" (no existe tabla para ese rol).\n");
                        continue; // saltar fila
                }

                // ============================
                // 3) Validar correo duplicado
                // ============================
                String checkCorreoSQL = "SELECT COUNT(*) FROM usuario WHERE correo = ?";
                try (PreparedStatement psCheckCorreo = conn.prepareStatement(checkCorreoSQL)) {
                    psCheckCorreo.setString(1, correo);
                    try (ResultSet rsCorreo = psCheckCorreo.executeQuery()) {
                        if (rsCorreo.next() && rsCorreo.getInt(1) > 0) {
                            errores.append("El correo ").append(correo)
                                    .append(" ya está registrado. Fila omitida.\n");
                            continue;
                        }
                    }
                }

                // ============================
                // 4) Validar CC duplicada
                // ============================
                String checkCcSQL = "SELECT COUNT(*) FROM vista_cc_global WHERE cc = ?";
                try (PreparedStatement psCheckCc = conn.prepareStatement(checkCcSQL)) {
                    psCheckCc.setString(1, cc);
                    try (ResultSet rsCc = psCheckCc.executeQuery()) {
                        if (rsCc.next() && rsCc.getInt(1) > 0) {
                            errores.append("La cédula ").append(cc)
                                    .append(" ya está registrada. Fila omitida.\n");
                            continue;
                        }
                    }
                }

                // ============================
                // 5) Generar contraseña
                // ============================
                String contrasena = generarContrasena();

                try {
                    // ============================
                    // 6) Insertar en usuario
                    // ============================
                    int idUsuario = 0;
                    String sqlUsuario = "INSERT INTO usuario (correo, contrasena, id_roles, habilitado) "
                            + "VALUES (?, ?, ?, TRUE) RETURNING id_usuario";
                    try (PreparedStatement psUsuario = conn.prepareStatement(sqlUsuario)) {
                        psUsuario.setString(1, correo);
                        psUsuario.setString(2, contrasena);
                        psUsuario.setInt(3, idRol);

                        try (ResultSet rs = psUsuario.executeQuery()) {
                            if (rs.next()) {
                                idUsuario = rs.getInt("id_usuario");
                            } else {
                                errores.append("No se pudo obtener id_usuario para correo ")
                                        .append(correo).append("\n");
                                continue;
                            }
                        }
                    }

                    // ============================
                    // 7) Insertar en tabla de rol
                    // ============================
                    String sqlInsert;
                    if (idRol == 6) { // profesor
                        sqlInsert = "INSERT INTO profesor (nombre, apellido, telefono, cc, id_usuario) "
                                + "VALUES (?, ?, ?, ?, ?)";
                    } else {          // estudiante
                        sqlInsert = "INSERT INTO estudiante (nombre, apellido, telefono, cc, id_usuario) "
                                + "VALUES (?, ?, ?, ?, ?)";
                    }

                    try (PreparedStatement psExtra = conn.prepareStatement(sqlInsert)) {
                        psExtra.setString(1, nombre);
                        psExtra.setString(2, apellido);
                        psExtra.setString(3, telefono);
                        psExtra.setString(4, cc);
                        psExtra.setInt(5, idUsuario);
                        psExtra.executeUpdate();
                    }

                    // ============================
                    // 8) Insertar en HISTORIAL
                    // ============================
                    String sqlHist = "INSERT INTO historial (id_usuario, id_roles, fecha_inicio) "
                            + "VALUES (?, ?, NOW())";
                    try (PreparedStatement psHist = conn.prepareStatement(sqlHist)) {
                        psHist.setInt(1, idUsuario);
                        psHist.setInt(2, idRol);
                        psHist.executeUpdate();
                    }

                    // ============================
                    // 9) Agregar fila a la tabla visual
                    // ============================
                    modelo.addRow(new Object[]{nombre, apellido, telefono, cc, correo, rolTexto});

                } catch (SQLException exFila) {
                    errores.append("Error al insertar usuario con correo ")
                            .append(correo).append(": ")
                            .append(exFila.getMessage()).append("\n");
                    exFila.printStackTrace();
                }
            }

            conn.close();

            // Mensaje final
            StringBuilder msgFinal = new StringBuilder("Carga masiva finalizada.\n");
            msgFinal.append("Revisa la tabla para ver los usuarios cargados.\n");
            if (errores.length() > 0) {
                msgFinal.append("\nSe omitieron algunas filas por errores:\n")
                        .append(errores);
            }

            JOptionPane.showMessageDialog(this, msgFinal.toString());

        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error leyendo el archivo: " + e.getMessage());
            e.printStackTrace();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error de base de datos: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnCargarActionPerformed

